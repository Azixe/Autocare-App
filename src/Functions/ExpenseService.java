    private void PrintBillButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PrintBillButtonActionPerformed
    String tmp = (String)VehicleChooser.getSelectedItem();
    Kendaraanid = tmp;
//    String tmp2 = (String)Model.getSelectedItem();
    
    try {
        // Calculate total service cost
        double totalCost = getTotalServiceCost(Kendaraanid);

        // Generate PDF report
        Document document = new Document();
        PdfWriter.getInstance(document, new FileOutputStream("ServiceReport_Vehicle_" + Kendaraanid + ".pdf"));
        document.open();

        // Add title
        Paragraph title = new Paragraph("Service Report for Vehicle ID: |" + Kendaraanid +"| "+Model.getText());
        title.setAlignment(Element.ALIGN_CENTER);
        document.add(title);

        // Add table
        PdfPTable table = new PdfPTable(5);
        addTableHeader(table);
        addRows(table, Kendaraanid);
        document.add(table);

        // Add total cost
        Paragraph total = new Paragraph("Total Cost: Rp." + totalCost);
        total.setAlignment(Element.ALIGN_RIGHT);
        document.add(total);

        document.close();

        JOptionPane.showMessageDialog(null, "PDF report generated successfully!");

    } catch (DocumentException | IOException | SQLException ex) {
        JOptionPane.showMessageDialog(null, "Error generating PDF: " + ex.getMessage());
    }
    }//GEN-LAST:event_PrintBillButtonActionPerformed

private void addTableHeader(PdfPTable table) {
            Stream.of("ID", "Tanggal Servis", "Catatan", "Jenis Servis", "Harga")
          .forEach(columnTitle -> {
              PdfPCell header = new PdfPCell();
              header.setPhrase(new Paragraph(columnTitle));
              table.addCell(header);
          });
}

private void addRows(PdfPTable table, String Kendaraanid) throws SQLException {
    String sql = "SELECT id, tanggalServis, catatanTambahan, jenisServis, harga FROM service WHERE vehicle_id = ?";
    try{
        conn=(java.sql.Connection)Functions.DatabaseConnection.getConnection();
        pst=conn.prepareStatement(sql);
    
        pst.setString(1,Kendaraanid);
        rs = pst.executeQuery();
            while (rs.next()) {
                table.addCell(String.valueOf(rs.getInt("id")));
                table.addCell(rs.getString("tanggalServis"));
                table.addCell(rs.getString("catatanTambahan"));
                table.addCell(rs.getString("jenisServis"));
                table.addCell(String.valueOf(rs.getDouble("harga")));
            }
        
    }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Data not found");
        }        // TODO add your handling code here:
    }          
    
