    public double getTotalServiceCost(String Kendaraanid) throws SQLException {
        try{
            String sql = "SELECT SUM(harga) AS totalCost FROM service WHERE vehicle_id = ?";
            pst = conn.prepareStatement(sql);
            pst.setString(1, Kendaraanid);;
            rs = pst.executeQuery();
                if (rs.next()) {
                    return rs.getDouble("totalCost");
                }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        return 0.0;
    }
    
